package Test;

public class day5 {

	public static void main(String[] args) {
		// Comparison operator
	System.out.println(14<4);
	System.out.println(14>4);
	System.out.println(14<=4);
	System.out.println(14>=4);
	System.out.println(14!=4);
	System.out.println(14==14);
	
	
	//logical operator 
	//And--&&
	System.out.println(4==4 && 5==5);
	System.out.println(4!=4 && 5==5);
	System.out.println(4==4 && 5!=5);
	System.out.println(4!=4 && 5!=5);
	
	
	//OR ---||
	System.out.println(10==9 ||10==9 );
	System.out.println(10==10 ||10==9);
	System.out.println(9==8 || 6==6);
	System.out.println(5==5 || 6==6);
	 
	// Not--!
	System.out.println(!(10==10));
	
	System.out.println(!(7==6));
	
	
	
	
	
	
	

	
	
	

	
	
		

	}

}
